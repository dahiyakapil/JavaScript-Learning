/*
    What is the easiest way to resize an array

    The length property of an array is useful to resize or empty an array quickly. Let's apply length property on number array to resize the number of elements from 5 to 3,
*/

const arr = [1, 2, 3, 4, 5];
console.log(arr.length);
arr.length = 3;
console.log(arr.length);
